package 刘怡东0717;

public class 学生打饭 {
private String name;
private String sex;
private int eat;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getSex() {
	return sex;
}
public void setSex(String sex) {
	this.sex = sex;
}
public int getEat() {
	return eat;
}
public void setEat(int eat) {
	this.eat = eat;
}
public 学生打饭(String name, String sex, int eat) {
	super();
	this.name = name;
	this.sex = sex;
	this.eat = eat;
}

}
